// Concrete class Vikarn, a noble Kaurav
class Vikarn extends Kaurav {
    void fight() {
        System.out.println("Vikarn fights bravely but with righteousness.");
    }

    // Overriding obey() and kind() because Vikarn was noble

    void obey() {
        System.out.println("Vikarn is obedient.");
    }


    void kind() {
        System.out.println("Vikarn is kind.");
    }
}